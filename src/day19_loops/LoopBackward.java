package day19_loops;

public class LoopBackward {
    public static void main(String[] args) {

        for(int i = 50; i >= 0; i--){
            System.out.print(i + " ");
        }

        System.out.println();

        for(char letter = 'A'; letter <= 'Z'; letter++){
            System.out.print(letter + " ");
        }





    }
}
